    private void btnReadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReadActionPerformed
        try {
 
            String nameNumberString;
            String name;
            long number;
            int index;
 
            // Using file pointer creating the file.
            File file = new File("friendsContact.txt");
 
            if (!file.exists()) {
 
                // Create a new file if not exists.
                file.createNewFile();
            }
 
            // Opening file in reading and write mode.
 
            RandomAccessFile raf = new RandomAccessFile(file, "rw");
            boolean found = false;
 
            // Traversing the file
            // getFilePointer() give the current offset
            // value from start of the file.
            while (raf.getFilePointer() < raf.length()) {
 
                // reading line from the file.
                nameNumberString = raf.readLine();
 
                // splitting the string to get name and
                // number
                String[] lineSplit
                    = nameNumberString.split("!");
 
                // separating name and number.
                name = lineSplit[0];
                number = Long.parseLong(lineSplit[1]);
 
                // Print the contact data
                System.out.println(
                    "Friend Name: " + name + "\n"
                    + "Contact Number: " + number + "\n");
            }
 
        }catch (Exception e) {
            System.out.println(e);
        }
  
    }//GEN-LAST:event_btnReadActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
         try {
         String newName = lblNombre.getText();
         long newNumber = Long.parseLong(lblNumero.getText());
         String nameNumberString;
         String name;
         long number;
         boolean found = false;
         
         File file = new File("friendsContact.txt");
         if (!file.exists()) {
             file.createNewFile();
         }

         RandomAccessFile raf = new RandomAccessFile(file, "rw");
         File tmpFile = new File("temp.txt");
         RandomAccessFile tmpRaf = new RandomAccessFile(tmpFile, "rw");

         // Read original file and modify if necessary
         while (raf.getFilePointer() < raf.length()) {
             nameNumberString = raf.readLine();
             String[] lineSplit = nameNumberString.split("!");
             name = lineSplit[0];
             number = Long.parseLong(lineSplit[1]);

             // If we find the name to update
             if (name.equals(newName)) {
                 nameNumberString = newName + "!" + String.valueOf(newNumber); // Update the record
                 found = true;
             }
             tmpRaf.writeBytes(nameNumberString);
             tmpRaf.writeBytes(System.lineSeparator());
         }

         // Replace original file with updated content
         if (found) {
             raf.setLength(0); // Clear original file
             tmpRaf.seek(0); // Go to start of temp file
             while (tmpRaf.getFilePointer() < tmpRaf.length()) {
                 raf.writeBytes(tmpRaf.readLine());
                 raf.writeBytes(System.lineSeparator());
             }
             txtEstado.setText("Contacto actualizado");
         } else {
             txtEstado.setText("Contacto no encontrado");
         }

         raf.close();
         tmpRaf.close();
     } catch (Exception e) {
         System.out.println(e);
     }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
         String nameToDelete = lblNombre.getText();  // Get the name to delete
         String nameNumberString;
         String name;
         boolean found = false;
         
         File file = new File("friendsContact.txt");
         if (!file.exists()) {
             file.createNewFile();
         }

         RandomAccessFile raf = new RandomAccessFile(file, "rw");
         File tmpFile = new File("temp.txt");
         RandomAccessFile tmpRaf = new RandomAccessFile(tmpFile, "rw");

         while (raf.getFilePointer() < raf.length()) {
             nameNumberString = raf.readLine();
             String[] lineSplit = nameNumberString.split("!");
             name = lineSplit[0];

             // If the name matches, don't write it to the temp file
             if (!name.equals(nameToDelete)) {
                 tmpRaf.writeBytes(nameNumberString);
                 tmpRaf.writeBytes(System.lineSeparator());
             } else {
                 found = true;
             }
         }

         if (found) {
             raf.setLength(0);  // Clear the original file
             tmpRaf.seek(0);  // Go to start of temporary file
             while (tmpRaf.getFilePointer() < tmpRaf.length()) {
                 raf.writeBytes(tmpRaf.readLine());
                 raf.writeBytes(System.lineSeparator());
             }
             txtEstado.setText("Contacto eliminado");
         } else {
             txtEstado.setText("Contacto no encontrado");
         }

         raf.close();
         tmpRaf.close();
         tmpFile.delete();  // Delete temporary file
     } catch (Exception e) {
         System.out.println(e);
     }
    }//GEN-LAST:event_btnDeleteActionPerformed

